    private void browseButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_browseButtonMouseClicked
         //Variable definition
        int returnVal;
        File file = null;
        JFileChooser fc;
         
        fc = new JFileChooser();

        returnVal = fc.showOpenDialog(LoadFile.this);
        if (returnVal == JFileChooser.APPROVE_OPTION) { 
           file = fc.getSelectedFile();
        }
        if(file != null && file.getPath() != null) {
            pathTxt.setText(file.getPath());
            StaticData.path = file.getPath();
        }
    }//GEN-LAST:event_browseButtonMouseClicked

