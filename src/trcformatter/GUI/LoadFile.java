  private void deleteAllMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteAllMouseClicked
    // Variable definition
    Thread th = new Thread(new QueriesDAO("DELETE", null, this, recordDates.getSelectedItem().toString()));
    
    th.start();
    try {
      th.join();
    } catch (InterruptedException ex) {
      Logger.getLogger(LoadFile.class.getName()).log(Level.SEVERE, null, ex);
    }
    JOptionPane.showMessageDialog(this, "Record Correctly Deleted", "An info message",
                                  JOptionPane.INFORMATION_MESSAGE);

    setRecordDates();
  }//GEN-LAST:event_deleteAllMouseClicked

