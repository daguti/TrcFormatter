    private void setRecordDates() {
      //Variable definition
      boolean data = false;
      
      Thread th = new Thread(new QueriesDAO("FECHAS", null, this, null));
    
      th.start();
      try {
        th.join();
      } catch (InterruptedException ex) {
        Logger.getLogger(LoadFile.class.getName()).log(Level.SEVERE, null, ex);
      }
      recordDates.removeAllItems();
      if(StaticData.fechas != null) {
        for(String opt : StaticData.fechas) {
          recordDates.addItem(opt);
          data = true;
        }
      }
      if(data) recordDates.setSelectedIndex(0);
    }

